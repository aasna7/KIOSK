public void displaying()
{
    Kiosk_Log klog = new Kiosk_Log();
    String userview=klog.studentlog_;
    try{
            Class.forName("oracle.jdbc.driver.OracleDriver");
            con = DriverManager.getConnection("jdbc:oracle:thin:@localhost:1521:orcl", "system", "123456");
            stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM PROFILE WHERE USERNAME='"+userview+"'");
            if (rs.next())
            {
                fullnameview.setText(rs.getString("FULL_NAME"));
                levelview.setText(rs.getString("STUDENT_LEVEL"));
                contactview.setText(rs.getString("CONTACT"));
                addressview.setText(rs.getString("ADDRESS"));
            }
            
            else
            {
                System.out.println("No data");
            }
             
            
            }
        catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "ERROR");
        }
}
